    private int hashString(String in) {
        int hash = 0;
        int multiplier = 1;
        for (byte val : in.getBytes()) {
            hash += (val & 0xff) * multiplier;
            int shifted = multiplier << 5;
            multiplier = shifted - multiplier;
        }

        return hash;
    }

